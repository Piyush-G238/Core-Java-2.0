package com.piyush.java.operators.arithmetic;

public class BasicArithmetic {
    public static void main(String[] args) {

//      a stores 2
        int a = 1 + 1;

//      b will store 2 * 3 = 6
        int b = a * 3;

//      c will store 6 / 4 = 1
        int c = b / 4;

//      d will store 1 - 2 = -1
        int d = c - a;

//      e will store -(-1) = 1
        int e = -d;

//      x will be storing the remainder 2
        int x = 42 % 10;

//      y will be storing remainder 2.25
        double y = 42.25 % 10;

    }
}
